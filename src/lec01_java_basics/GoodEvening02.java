package lec01_java_basics;

// we will learn print vs println
// println is mostly used

public class GoodEvening02 {
	public static void main(String[] args) {
		// Here System is a class, out is a properties, print() is a method
		System.out.print("Good Evening! ");
		System.out.print("How are you feeling? ");
		System.out.print("Welcome to Enthrall IT");
		
		// println -- print line by line
		// print -- print continuously, not line by line
		// you can make it readable by putting a space at the end
	}

}
