package lec02_java_basics;

public class F_use_of_single_or_double_quotation_inside_String {

	public static void main(String[] args) {
		System.out.println("I am Tofael, I am your instructor.");
		System.out.println("I am \'Tofael\', I am your instructor."); // you need to know it
		System.out.println("I am \"Tofael\", I am your instructor."); // you need to know it
		System.out.println("I am \\Tofael\\, I am your instructor.");
	}

}
